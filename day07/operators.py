# Bitwise operators: & | ^ << >> ~
# &
print(5 & 6)
# 101 & 110 ==> 100 = 4
print(11&7)
# 1011 & 0111 ==> 3

# |
print(5|6)
#101 | 110 ==> 111  => 7
print(11|7)
# 1011 | 0111 ==> 1111 = 15

# xor
print(5^6)
#101 ^ 110 = 011 => 3
print(11^7)
# 1011 ^  0111 ==> 1100 = 12

# ~
print(~12) #-13
print(~-12) #11
print(~15) #-16   one will be added and - sign is added
# 1st step complementary and secondanry ex...

# left and right shift 
print(5<<1)  #5*2 lift shift
print(5>>1) #5//2 right shift

print(4>>1) #4//2
print(4<<1)  #4*2

print(17 << 1)  #17*2
print(17 >> 1)  #17//2


